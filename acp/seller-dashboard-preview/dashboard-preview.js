async function handlePrePhoto(file) {
  if (!file) {
    state.photoDataUrl = "";
    state.photoName = "";
    state.preForm.photoDataUrl = "";
    state.preForm.photoName = "";
    return;
  }
  if (!isImageFile(file)) return;

  state.aiLoading = true;
  state.aiInsight = null;
  state.preReport = null;
  renderPre();

  try {
    state.photoDataUrl = await preparePhotoDataUrl(file);
    state.photoName = file.name;
    const imageOnly = { ...emptyPreForm(), photoName: file.name, photoDataUrl: state.photoDataUrl };
    const insight = await requestSellerAi("pre", imageOnly, {}, state.photoDataUrl);
    const resolvedInsight = insight || localPhotoFallbackInsight();
    const draftForm = buildPreDraftForm(imageOnly, resolvedInsight);
    draftForm.photoName = file.name;
    draftForm.photoDataUrl = state.photoDataUrl;
    state.preDraft = { form: draftForm, insight: resolvedInsight };
    state.preReport = null;
    state.aiInsight = resolvedInsight;
    state.aiFilledFields = aiRecommendedFields(resolvedInsight);
  } catch {
    const fallback = localPhotoFallbackInsight();
    const draftForm = buildPreDraftForm(emptyPreForm(), fallback);
    state.preDraft = { form: draftForm, insight: fallback };
    state.aiInsight = fallback;
  } finally {
    state.aiLoading = false;
    renderPre();
  }
}

function cancelPreLaunchProduct() {
  const has = Object.values(state.preForm).some((v) => String(v).trim()) || state.preReport || state.aiInsight;
  if (has && !window.confirm("Clear this pre-launch draft and choose a different product?")) return;
  state.preForm = emptyPreForm();
  state.preReport = null;
  state.aiInsight = null;
  state.pendingSafeChanges = [];
  state.aiFilledFields = [];
  state.photoDataUrl = "";
  state.photoName = "";
  renderPre();
}

async function requestSellerAi(mode, productInput, computedReport, photoDataUrl) {
  state.aiLoading = true;
  if (mode === "pre") renderPre();
  else renderPost();
  try {
    const response = await fetch("/api/seller-ai", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ mode, productInput, computedReport, photoDataUrl: photoDataUrl || undefined })
    });
    state.aiInsight = await response.json();
    return state.aiInsight;
  } catch {
    state.aiInsight = null;
    return null;
  } finally {
    state.aiLoading = false;
    if (mode === "pre") renderPre();
    else renderPost();
  }
}

