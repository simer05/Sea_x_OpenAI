function bindPreForm() {
  const form = byId("pre-form");
  if (!form) return;

  const fields = ["title", "category", "productType", "price", "launchStock", "shippingCost", "packagingCost", "adCost", "targetArea", "colors", "features", "description", "keywords"];
  const ids = { title: "pre-title", category: "pre-category", productType: "pre-product-type", price: "pre-price", launchStock: "pre-stock", shippingCost: "pre-shipping", packagingCost: "pre-packaging", adCost: "pre-ad-cost", targetArea: "pre-target-area", colors: "pre-colors", features: "pre-features", description: "pre-description", keywords: "pre-keywords" };

  fields.forEach((field) => {
    const el = byId(ids[field]);
    if (!el) return;
    el.addEventListener("input", () => {
      state.preForm[field] = el.value;
      state.aiFilledFields = state.aiFilledFields.filter((f) => f !== field);
      state.preReport = isPreFormAnalyzable(state.preForm) ? analyzePreLaunch(state.preForm) : null;
      state.aiInsight = null;
      state.pendingSafeChanges = [];
      renderPre();
    });
  });

  form.addEventListener("submit", (e) => {
    e.preventDefault();
    syncPreFormFromDom();
    state.preReport = analyzePreLaunch(state.preForm);
    state.aiLoading = true;
    renderPre();
    void requestSellerAi("pre", state.preForm, state.preReport, state.preForm.photoDataUrl).then(() => {
      state.aiLoading = false;
      renderPre();
    });
  });

  const photo = byId("prelaunch-photo-upload");
  if (photo) photo.addEventListener("change", (e) => void handlePrePhoto(e.target.files?.[0] || null));

  const ruleBtn = byId("rule-safe-btn");
  if (ruleBtn) ruleBtn.addEventListener("click", () => {
    if (!state.preReport) return;
    state.preForm = applySafePreLaunchChanges(state.preForm, state.preReport);
    syncPreFormToDom();
    state.preReport = analyzePreLaunch(state.preForm);
    renderPre();
  });

  const previewBtn = byId("preview-ai-btn");
  if (previewBtn) previewBtn.addEventListener("click", () => {
    if (!state.aiInsight?.safeChanges?.length) return;
    state.pendingSafeChanges = state.aiInsight.safeChanges;
    renderPre();
  });

  const confirmBtn = byId("confirm-safe-btn");
  if (confirmBtn) confirmBtn.addEventListener("click", () => {
    if (!state.pendingSafeChanges.length) return;
    if (!window.confirm(`Apply ${state.pendingSafeChanges.length} safe AI-recommended change(s)? Risky edits like title, category, price, and photo will remain blocked.`)) return;
    state.pendingSafeChanges.forEach((change) => {
      if (SAFE_FIELDS.has(change.field)) state.preForm[change.field] = change.value;
    });
    syncPreFormToDom();
    state.preReport = analyzePreLaunch(state.preForm);
    state.pendingSafeChanges = [];
    renderPre();
  });

  const cancelBtn = byId("cancel-pre-btn");
  if (cancelBtn) cancelBtn.addEventListener("click", cancelPreLaunchProduct);

  const addListingBtn = byId("add-listing-btn");
  if (addListingBtn) addListingBtn.addEventListener("click", addPreListingToPostLaunch);
}

