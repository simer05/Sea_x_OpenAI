function confirmPreDraft() {
  if (!state.preDraft) return;
  state.preForm = { ...state.preDraft.form };
  state.preReport = analyzePreLaunch(state.preForm);
  state.aiInsight = state.preDraft.insight;
  state.aiFilledFields = aiRecommendedFields(state.preDraft.insight);
  state.preDraft = null;
  syncPreFormToDom();
  renderPre();
  void requestSellerAi("pre", state.preForm, state.preReport, state.preForm.photoDataUrl);
}

function startManualPreEntry() {
  if (state.preDraft) {
    state.preForm = { ...state.preDraft.form };
    syncPreFormToDom();
  }
  state.preDraft = null;
  state.preReport = state.preForm.title ? analyzePreLaunch(state.preForm) : null;
  renderPre();
}

