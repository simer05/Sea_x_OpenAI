function init() {
  const select = byId("timeframe-select");
  select.innerHTML = timeframes.map((t, i) => `<option value="${i}">${escapeHtml(t.label)}</option>`).join("");
  select.value = String(state.timeframeIndex);
  select.addEventListener("change", (e) => {
    state.timeframeIndex = Number(e.target.value);
    state.aiInsight = null;
    render();
  });

  document.querySelectorAll("[data-mode]").forEach((btn) => {
    btn.addEventListener("click", () => setMode(btn.dataset.mode));
  });

  render();
}

function setMode(mode) {
  const next = mode === "pre" ? "pre" : "post";
  if (next === "pre" && state.mode !== "pre" && !state.photoDataUrl) {
    state.preReport = null;
    state.aiInsight = null;
    state.preForm = emptyPreForm();
    state.aiFilledFields = [];
  }
  state.mode = next;
  render();
}

function render() {
  document.querySelectorAll("[data-mode]").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.mode === state.mode);
  });
  byId("post-mount").classList.toggle("hidden", state.mode !== "post");
  byId("pre-mount").classList.toggle("hidden", state.mode !== "pre");
  byId("post-head-actions").classList.toggle("hidden", state.mode !== "post");

  text("workspace-title", state.mode === "post" ? "Post-Launch Seller Intelligence" : "Pre-Launch Seller Intelligence");
  text("seller-perspective-copy", state.mode === "post"
    ? "Optimize live listings, reviews, and campaign spend"
    : "Validate new listing ideas before launch");
  text("page-title", state.mode === "post" ? "Post-Launch Product Dashboard" : "Pre-Launch Product Validation");
  text("page-copy", state.mode === "post"
    ? "Choose a live seller product to generate product health, market trend, competitor, review, and action dashboards."
    : "Enter a new product idea to estimate launch fit, comparison pressure, Singapore target area, and initial stock size.");

  if (state.mode === "post") renderPost();
  else renderPre();
}

